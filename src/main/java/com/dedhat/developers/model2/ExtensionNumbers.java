package com.dedhat.developers.model2;

public class ExtensionNumbers
{
	
	private String ext1;
	private String ext2;
	private String ext3;
	private String ext4;
	
	
	
	public ExtensionNumbers(String ext1, String ext2, String ext3, String ext4) {
		super();
		this.ext1 = ext1;
		this.ext2 = ext2;
		this.ext3 = ext3;
		this.ext4 = ext4;
	}
	public String getExt1() {
		return ext1;
	}
	public void setExt1(String ext1) {
		this.ext1 = ext1;
	}
	public String getExt2() {
		return ext2;
	}
	public void setExt2(String ext2) {
		this.ext2 = ext2;
	}
	public String getExt3() {
		return ext3;
	}
	public void setExt3(String ext3) {
		this.ext3 = ext3;
	}
	public String getExt4() {
		return ext4;
	}
	public void setExt4(String ext4) {
		this.ext4 = ext4;
	}		
	
}